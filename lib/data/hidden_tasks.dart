// Shuffle queues keyed by "modeId_lang" so each mode+language gets its own
// exhaustion cycle — no task repeats until all tasks have been seen once.
final Map<String, List<int>> _taskShuffleQueues = <String, List<int>>{};
final Random _taskRandom = Random();

int pickHiddenTaskIndex(String modeId, int listLength) {
  if (listLength <= 1) return 0;
  final String key = '${modeId}_${AppLocale.isEn ? 'en' : 'ru'}';
  List<int> queue = _taskShuffleQueues[key] ?? <int>[];
  if (queue.isEmpty) {
    queue = List<int>.generate(listLength, (int i) => i)..shuffle(_taskRandom);
    _taskShuffleQueues[key] = queue;
  }
  return queue.removeLast();
}
